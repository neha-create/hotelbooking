package com.hotelbooking.BO;

public class Contact
{
    private String phone;

    private String fax;

    public void setPhone(String phone){
        this.phone = phone;
    }
    public String getPhone(){
        return this.phone;
    }
    public void setFax(String fax){
        this.fax = fax;
    }
    public String getFax(){
        return this.fax;
    }
}
